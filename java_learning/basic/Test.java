package main;

public class Test {

    public static void main(String[] args) {
        System.out.printf("%d\n", 5 / -3);
        System.out.printf("%d\n", 5 / -3);
        System.out.printf("%d\n", -5 / 3);

        System.out.printf("%d\n", 5 % 3);   // 2
        System.out.printf("%d\n", -5 % 3);  // -2
        System.out.printf("%d\n", 5 % -3);  // 2
    }

}
